package exerciciosDia_04_02_2021.exercicio4.Outros;

public class Retroprojetor {

	private double preco;
	private double qualidadeVideo;
	public Retroprojetor(double preco, double qualidadeVideo) {
		super();
		this.preco = preco;
		this.qualidadeVideo = qualidadeVideo;
	}
	public double getPreco() {
		return preco;
	}
	public void setPreco(double preco) {
		this.preco = preco;
	}
	public double getQualidadeVideo() {
		return qualidadeVideo;
	}
	public void setQualidadeVideo(double qualidadeVideo) {
		this.qualidadeVideo = qualidadeVideo;
	}
	
	
}
